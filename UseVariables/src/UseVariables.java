public class UseVariables {
    public static void main(String[] args) {
        int i = 10;
        float f = 20.5f;
        double d = 20.5;
        boolean b = true;
        char c = 'a';
        String s = "Hà Nội";

        System.out.println("kiểu số nguyên i = " + i);
        System.out.println("kiểu số thực f = " + f);
        System.out.println("Kiểu số thực lớn = " + d);
        System.out.println("Kiểu loigc b = " + b);
        System.out.println("Kiểu ký tự c = " + c);
        System.out.println("Kiểu chuỗi b = " + s);
    }
}
